                <div class="col-12">
                    <p class="lead">The Legal Affairs Committee of the European Parliament has accepted the draft directive on copyright on the digital market. The next vote can take place even on July 3-5. The European Commission intends to introduce a new tool, allegedly to protect copyrights, and in fact to subordinate the information market to the largest media corporations.</p>
                    <hr />
                    <p class="lead">Most controversial articles are:</p>
                    <ul>
                        <li class="lead"><strong>Art. 11</strong> – introduces a compulsory payment for sharing materials online, this means stopping information flow, getting rid of small publishers and limiting even linking to websites (so-called "link tax").</li>
                        <li class="lead"><strong>Art. 13</strong> – it concerns the owners of online platforms who will have to ensure the legality of posted content. This means that the content of platform users, such as Facebook, Twitter, Instagram, will be obligatorily monitored. This directly affects the freedom to exchange information, the right to privacy or freedom of speech.</li>
                    </ul>
