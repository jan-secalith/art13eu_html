                    <p class="lead">Komisja Prawna Parlamentu Europejskiego zaakceptowała projekt dyrektywy o prawie autorskim na rynku cyfrowym. Kolejne głosowanie może się odbyć już nawet 3-5 lipca. Komisja Europejska zamierza wprowadzić nowe narzędzie, rzekomo do ochrony praw autorskich, a w rzeczywistości do podporządkowania rynku informacji największym korporacjom medialnym.</p>
                    <hr />
                    <p class="lead">Najbardziej kontrowersyjne są dwa artykuły:</p>
                    <ul>
                        <li class="lead"><strong>Art. 11</strong> – wprowadza obowiązkową opłatę za dzielenie się materiałami w sieci, oznacza to zahamowanie przepływu informacji, pozbycie się małych wydawców oraz ograniczenie nawet linkowania do stron (tzw. „podatek od linków”).</li>
                        <li class="lead"><strong>Art. 13</strong> – dotyczy właścicieli platform internetowych, którzy będą musieli zapewnić o legalności zamieszczonych treści. Oznacza to, że treści użytkowników platform takich jak np. Facebook, Twitter, Google czy Onet, Wirtualna Polska, będą obowiązkowo monitorowane. Bezpośrednio uderza to w swobodę wymiany informacji, prawo do prywatność czy wolność wypowiedzi.</li>
                    </ul>
